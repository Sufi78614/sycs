fun add(n1:Int,n2:Int):Int
{
	var ans = n1 + n2
	return ans
}

fun subtract(n1:Int,n2:Int):Int
{
	var ans = n1 - n2
	return ans
}

fun multiply(n1:Int,n2:Int):Int
{
	var ans = n1 * n2
	return ans
}

fun divide(n1:Int,n2:Int):Int
{
	var ans = n1 / n2
	return ans
}

